    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String card = jTextField1.getText();
        String name = jTextField2.getText();
        String surname = jTextField3.getText();
        String department = jTextField4.getText();
        
        Canteen_manager ap=new Canteen_manager();
        if(ap.addEmployee(card,name,surname,department))
                {
                    JOptionPane.showMessageDialog(null,"Employee Successfully Added");
            
                    //clear textbox
                    DefaultTableModel dm= new Canteen_manager().getEmployeeData();
                    jTable1.setModel(dm);
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jTextField4.setText("");
                   
                }else
                {
                   JOptionPane.showMessageDialog(null,"Not Updated");
                }
    }//GEN-LAST:event_jButton1ActionPerformed

