    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String card = jTextField1.getText();
        String name = jTextField2.getText();
        String surname = jTextField3.getText();
        String department = jTextField4.getText();
        
        Canteen_manager ap=new Canteen_manager();
        try {
            if(ap.updateEmployee(card, name, surname, department))
            {
                JOptionPane.showMessageDialog(null,"Successfull Updated");
                
                //clear textbox
                DefaultTableModel dm= new Canteen_manager().getEmployeeData();
                jTable1.setModel(dm);
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                
            }else
            {
                JOptionPane.showMessageDialog(null,"Not Updated");
            }
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null,ex.toString());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String[] options={"YES","NO"};
        int ans=JOptionPane.showOptionDialog(null,"Sure To Delete??","Delete Confirm", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE,null,options, options[1]);
        
        if(ans==0)
        {
            String card = jTextField1.getText();
            
            
            
            if(new Canteen_manager().deleteEmployee(card))
        {
            JOptionPane.showMessageDialog(null,"Successfull Deleted");
            
            //clear textbox
            DefaultTableModel dm= new Canteen_manager().getEmployeeData();
            jTable1.setModel(dm);
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
           
        }else
        {
            JOptionPane.showMessageDialog(null,"Failed Deleted");
        }
        }else{
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

